
/* Adição */

function somar(num1, num2) {
  return num1 + num2
}

const resultadoSoma = somar(1,3)
console.log('resultado da soma: '+resultadoSoma)

/* Subtração */

function subtrair(num1, num2) {
  return num1 - num2
}

const resultadoSubtração = subtrair(15,3)
console.log('resultado da subtração: '+resultadoSubtração)

/* Multiplicação */

function multiplicar(num1, num2) {
  return num1 * num2
}

const resultadoMultiplicacao = multiplicar(15,3)
console.log('resultado da multiplicação: '+resultadoMultiplicacao)

/* Divisão */

function divisao(num1, num2) {
  return num1 / num2
}

const resultadoDivisao = divisao(15,3)
console.log('resultado da divisão: '+resultadoDivisao)